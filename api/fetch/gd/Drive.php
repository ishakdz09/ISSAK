        $this->_getCache($this->gdid,json_encode($gd));
        }
        $p = $gd["source"];
        $p = urldecode(explode("&",explode("&fmt_stream_map=",$p)[1])[0]);
        $p = explode(",",$p);
        foreach($p as $w){
            $r = explode("|",$w);
            $link[$r[0]] = $r[1];
        }
        $this->title =  pathinfo(urldecode(explode("&",explode("&title=",$gd["source"])[1])[0]), PATHINFO_FILENAME);
        $this->cookie = implode('; ', $gd["cookie"]);
        $this->link = $link;
    }
